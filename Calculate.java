//Q3.Write a java program to calculate the sum of all elements in an integer array?

public class Calculate 
{
    public static void main(String args[])
    {
        int arr[] ={2,4,6,8,3,6,2,4,6,5};
        int n=0;
        for(int i=0; i<10; i++)
        {
            n+=arr[i];
        }
        System.out.println(n);
    }    
}
